package com.spring.dto;

public class LocationVO {
	private int no;
	private String location;
	private String detect;
	private String cctv_person;
	private String cctv_car;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getDetect() {
		return detect;
	}
	public void setDetect(String detect) {
		this.detect = detect;
	}
	public String getCctv_person() {
		return cctv_person;
	}
	public void setCctv_person(String cctv_person) {
		this.cctv_person = cctv_person;
	}
	public String getCctv_car() {
		return cctv_car;
	}
	public void setCctv_car(String cctv_car) {
		this.cctv_car = cctv_car;
	}
	
	
}
